package uz.todoapp.web.dto;

public class RoleDTO {

    private Long id;
    private String name;

    // Constructor
    public RoleDTO() {
    }

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

